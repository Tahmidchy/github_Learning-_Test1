// Answer: fetch ফাংশনটি একটি API যা ওয়েব থেকে ডাটা রিকোয়েস্ট এবং রিসিভ করার জন্য ব্যবহার করা হয়, এবং এটি একটি প্রমিজ রিটার্ন করে। প্রমিজ হল একটি অবজেক্ট যা অ্যাসিঙ্ক্রোনাস অপারেশনের ফলাফল প্রতিনিধিত্ব করে, যা সফল বা ব্যর্থ হতে পারে। সহজভাবে বলতে গেলে, fetch হল একটি ফাংশন যা প্রমিজ ব্যবহার করে ডাটা ফেচ করার কাজ করে।
fetch('https://jsonplaceholder.typicode.com/posts/1')
    .then(response => response.json()) // রেসপন্স থেকে JSON ডাটা এক্সট্রাক্ট করা হচ্ছে
    .then(data => console.log(data)) // কনভার্ট করা ডাটা কনসোলে লগ করা হচ্ছে
    .catch(error => console.error('Error:', error));